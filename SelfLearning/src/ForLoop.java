import java.util.Scanner;

public class ForLoop {
    static void main(String args[])
    {

        for (int i = 1; i <= 3; i++)
            for (int j = 1; j <= 3; j++)
            {
                //System.out.print("* ");
                System.out.print( "i =" + i + " "+ "j = " + j + " ");
            }
        System.out.println();
    }
}
